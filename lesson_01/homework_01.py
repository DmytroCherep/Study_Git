print("Надвечір потепліло на 4°C.")
print(f"Отже, надвечір температура: {evening_temp}°C.")

# task 09
"""
Взагалі у театральному гуртку - 24 хлопчики, а дівчаток - вдвічі менше.
1 хлопчик захворів та 2 дівчинки не прийшли сьогодні.
Скількі сьогодні дітей у театральному гуртку?
"""

boys = 24
girls = boys // 2          # дівчаток вдвічі менше
boys_today = boys - 1      # 1 хлопчик захворів
girls_today = girls - 2    # 2 дівчинки не прийшли
total_today = boys_today + girls_today

print("\nЗАДАЧА 9:")
print(f"Всього хлопчиків: {boys}, дівчаток: {girls}.")
print(f"Сьогодні прийшло хлопчиків: {boys_today}, дівчаток: {girls_today}.")
print(f"Разом дітей сьогодні у гуртку: {total_today}.")

