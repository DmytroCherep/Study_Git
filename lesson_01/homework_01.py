# task 08
"""
До обіда температура повітря була на 5 градусів вище нуля.
Після обіду температура опустилася на 10 градусів.
Надвечір потепліло на 4 градуси. Яка температура надвечір?
"""

temp = 5       # до обіду +5
temp = temp - 10  # після обіду стало холодніше на 10
temp = temp + 4   # надвечір потепліло на 4
evening_temp = temp

print("\nЗАДАЧА 8:")
print("До обіду температура була +5°C.")
